package beans;

public class BuyDetailBeans {
	private int id;
	private int buyId;
	private int itemId;
	private int number;
    private int g;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getBuyId() {
		return buyId;
	}
	public void setBuyId(int buyId) {
		this.buyId = buyId;
	}
	public int getItemId() {
		return itemId;
	}
	public void setItemId(int itemId) {
		this.itemId = itemId;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public int getG() {
		return g;
	}
	public void setG(int g) {
		this.g = g;
	}
}
